import pandas as pd

# Создаем DataFrame с оценками учеников
data = {
    'Ученик': ['Иван', 'Мария', 'Алексей', 'Елена', 'Дмитрий', 'Ольга', 'Сергей', 'Наталья', 'Павел', 'Анастасия'],
    'Математика': [4, 3, 5, 2, 4, 3, 5, 4, 3, 4],
    'Физика': [3, 4, 4, 5, 3, 3, 4, 5, 3, 3],
    'Химия': [5, 4, 3, 4, 3, 4, 4, 5, 3, 4],
    'Литература': [4, 3, 4, 3, 3, 4, 4, 5, 4, 4],
    'История': [4, 3, 4, 5, 4, 3, 3, 4, 3, 4]
}

df = pd.DataFrame(data)

# Вывод первых нескольких строк DataFrame
print(df.head())

# Средняя оценка по каждому предмету
average_grades = df.mean(numeric_only=True)
print("\nСредняя оценка по каждому предмету:")
print(average_grades)

# Медианная оценка по каждому предмету
median_grades = df.median(numeric_only=True)
print("\nМедианная оценка по каждому предмету:")
print(median_grades)

# Вычисление Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
print(f"\nQ1 для оценок по математике: {Q1_math}")
print(f"Q3 для оценок по математике: {Q3_math}")

# Вычисление IQR для оценок по математике
IQR_math = Q3_math - Q1_math
print(f"IQR для оценок по математике: {IQR_math}")

# Стандартное отклонение для оценок по каждому предмету
std_dev = df.std(numeric_only=True)
print("\nСтандартное отклонение для оценок по каждому предмету:")
print(std_dev)
